# 5. Program to Read Two Numbers and Print Their Quotient and Remainder
a=int(input("Enter the value of a:"))
b=int(input("Enter the value of b:"))
print(f"The value of a is {a}")
print(f"The value of b  is {b}")

quotient=a//b
remainder=a%b
print(f"The quotient be:{quotient}")
print(f"The quotient be:{remainder}")
